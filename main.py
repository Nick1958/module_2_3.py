my_list = [42, 69, 322, 13, 0, 99, -5, 9, 8, 7, -6, 5]
L = len(my_list)          # длина списка или количество чисел в списке, в нашем случае равно 12
i = 0
while i < L:
    if my_list [i] > 0:
        print(my_list [i])
    elif my_list [i] < 0:
        break
    i += 1